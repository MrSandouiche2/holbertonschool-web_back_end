#!/usr/bin/env python3
"""
Module to convert a float to its string representation.

This module contains a function `to_str` that takes a float as input and return
the string representation of that float.
"""


def to_str(n: float) -> str:
    """
    Convert a float to its string representation.

    Args:
        n (float): The float value to convert.

    Returns:
        str: The string representation of the input float.

    Example:
        >>> to_str(3.14)
        '3.14'
    """
    return str(n)
